package com.demo.pojo;

public class Download{
	private int downloadId;

	private int downloadUId;
	
	private int downloadPoetryId;
	
	public int getDownloadId() {
		return downloadId;
	}
	public void setDownloadId(int downloadId) {
		this.downloadId = downloadId;
	}
	
	public int getDownloadUId() {
		return downloadUId;
	}
	public void setDownloadUId(int downloadUId) {
		this.downloadUId = downloadUId;
	}
	
	public int getDownloadPoetryId() {
		return downloadPoetryId;
	}
	public void setDownloadPoetryId(int downloadPoetryId) {
		this.downloadPoetryId = downloadPoetryId;
	}
}